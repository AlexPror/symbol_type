"use strict";

/*
###Задание 1
Необходимо создать класс Library. Конструктор класса, должен принимать начальный 
список книг (массив) в качестве аргумента. Убедитесь, что предоставленный массив 
не содержит дубликатов; в противном случае необходимо выбросить ошибку.
1. Класс должен содержать приватное свойство #books, которое должно хранить 
книги, переданные при создании объекта.
2. Реализуйте геттер-функцию allBooks, которая возвращает текущий список книг.
3. Реализуйте метод addBook(title), который позволяет добавлять книгу в список. 
Если книга с таким названием уже существует в списке, выбросьте ошибку с 
соответствующим сообщением.
4. Реализуйте метод removeBook(title), который позволит удалять книгу из списка 
по названию. Если книги с таким названием нет в списке, выбросьте ошибку с 
соответствующим сообщением.
5. Реализуйте метод hasBook(title), который будет проверять наличие книги в 
библиотеке и возвращать true или false в зависимости от того, есть ли такая 
книга в списке или нет.
*/

class Library {
  #books;

  constructor(initialBooks) {
    // Проверяем, что в массиве нет дубликатов
    if (new Set(initialBooks).size !== initialBooks.length) {
      throw new Error("Массив книг содержит дубликаты");
    }
    this.#books = initialBooks;
  }

  get allBooks() {
    return this.#books;
  }

  addBook(title) {
    // Проверяем, что книги с таким названием еще нет в списке
    if (this.#books.includes(title)) {
      throw new Error(`Книга "${title}" уже есть в библиотеке`);
    }
    this.#books.push(title);
  }

  removeBook(title) {
    // Находим индекс книги в списке
    const index = this.#books.indexOf(title);
    if (index === -1) {
      throw new Error(`Книги "${title}" нет в библиотеке`);
    }
    // Удаляем книгу из списка
    this.#books.splice(index, 1);
  }

  hasBook(title) {
    return this.#books.includes(title);
  }
}

// Создаем библиотеку с начальным списком книг
const library = new Library([
  "Война и мир",
  "Преступление и наказание",
  "Анна Каренина",
]);

// Получаем список всех книг
console.log(library.allBooks); // ["Война и мир", "Преступление и наказание", "Анна Каренина"]

// Добавляем новую книгу
library.addBook("Мастер и Маргарита");
console.log(library.allBooks); // ["Война и мир", "Преступление и наказание", "Анна Каренина", "Мастер и Маргарита"]

// Пытаемся добавить книгу, которая уже есть в библиотеке
try {
  library.addBook("Война и мир");
} catch (error) {
  console.log(error.message); // "Книга "Война и мир" уже есть в библиотеке"
}

// Удаляем книгу из библиотеки
library.removeBook("Преступление и наказание");
console.log(library.allBooks); // ["Война и мир", "Анна Каренина", "Мастер и Маргарита"]

// Пытаемся удалить книгу, которой нет в библиотеке
try {
  library.removeBook("Идиот");
} catch (error) {
  console.log(error.message); // "Книги "Идиот" нет в библиотеке"
}

// Проверяем, есть ли книга в библиотеке
console.log(library.hasBook("Анна Каренина")); // true
console.log(library.hasBook("Преступление и наказание")); // false
